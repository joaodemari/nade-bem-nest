export default (level: number) => {
  switch (level) {
    case 1:
      return "lambari-1.png";
    case 2:
      return "sardinha-2.png";
    case 3:
      return "cacao-3.png";
    case 4:
      return "golfinho-4.png";
    case 5:
      return "tubarao-5.png";
    default:
      return "lambari-1.png";
  }
};
